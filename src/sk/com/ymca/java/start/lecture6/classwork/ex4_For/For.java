package sk.com.ymca.java.start.lecture6.classwork.ex4_For;

public class For {
    public static void main(String[] args) {

        /*
        Циклічна конструкція for з лічильником
        for (Ініціалізація лічильника; Умова; Зміна лічильника)
        {Тіло циклу}
         */

        for (int i = 0; i <= 3; i++){
            System.out.println("i = " + i);
        }

//      System.out.println("i = " + i);

    }
}
